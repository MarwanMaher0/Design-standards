        localStorage.setItem(`Oth-row-${row.id}-checkbox-${checkboxIndex}`, checkbox.checked);
        toggleSvgDisplay(checkbox, rowIndex);
    });

    updateRowRefsBasedOnCheckboxState();
    updateColumnSums();
};



